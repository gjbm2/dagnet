function numOr<T extends number>(value: unknown, fallback: T): number {
  return (typeof value === 'number' && Number.isFinite(value)) ? value : fallback;
}

/**
 * Reads shared forecasting knobs from settings/settings.yaml (stored in IndexedDB).
 *
 * Critical properties:
 * - Safe fallbacks to the compiled defaults (constants/latency.ts)
 * - Does NOT throw if settings file is missing or malformed (fetches must remain usable)
 */
class ForecastingSettingsService {
  async getForecastingModelSettings(): Promise<ForecastingModelSettings> {
    // Vitest frequently uses fake timers in tests (vi.useFakeTimers), which can
    // interact badly with IndexedDB/Dexie async scheduling and cause hangs.
    // For test determinism and reliability, default to compiled constants unless
    // a test explicitly exercises settings persistence elsewhere.
    //
    // This keeps the production behaviour unchanged, while ensuring tests that
    // pin time via fake timers don't deadlock waiting on IDB.
    if (typeof process !== 'undefined' && process?.env?.VITEST) {
      return {
        RECENCY_HALF_LIFE_DAYS, LATENCY_MIN_EFFECTIVE_SAMPLE_SIZE,
        DEFAULT_T95_DAYS, FORECAST_BLEND_LAMBDA,
        LATENCY_BLEND_COMPLETENESS_POWER, ANCHOR_DELAY_BLEND_K_CONVERSIONS,
        LATENCY_MAX_MEAN_MEDIAN_RATIO, ONSET_MASS_FRACTION_ALPHA,
        ONSET_AGGREGATION_BETA,
        BAYES_LOG_KAPPA_MU, BAYES_LOG_KAPPA_SIGMA,
        BAYES_FALLBACK_PRIOR_ESS, BAYES_DIRICHLET_CONC_FLOOR,
        BAYES_SIGMA_FLOOR, BAYES_MU_PRIOR_SIGMA_FLOOR,
        BAYES_MATURITY_FLOOR, BAYES_SOFTPLUS_SHARPNESS,
        BAYES_RHAT_THRESHOLD, BAYES_ESS_THRESHOLD,
        BAYES_WARM_START_RHAT_MAX, BAYES_WARM_START_ESS_MIN,
        BAYES_HDI_PROB, BAYES_DRAWS, BAYES_TUNE, BAYES_CHAINS,
        BAYES_TARGET_ACCEPT,
      };
    }
    try {
      const file = await db.files.get('settings-settings');
      const forecasting: any = (file as any)?.data?.forecasting || {};
      return {
        RECENCY_HALF_LIFE_DAYS: numOr(forecasting.RECENCY_HALF_LIFE_DAYS, RECENCY_HALF_LIFE_DAYS),
        LATENCY_MIN_EFFECTIVE_SAMPLE_SIZE: numOr(forecasting.LATENCY_MIN_EFFECTIVE_SAMPLE_SIZE, LATENCY_MIN_EFFECTIVE_SAMPLE_SIZE),
        DEFAULT_T95_DAYS: numOr(forecasting.DEFAULT_T95_DAYS, DEFAULT_T95_DAYS),
        FORECAST_BLEND_LAMBDA: numOr(forecasting.FORECAST_BLEND_LAMBDA, FORECAST_BLEND_LAMBDA),
        LATENCY_BLEND_COMPLETENESS_POWER: numOr(forecasting.LATENCY_BLEND_COMPLETENESS_POWER, LATENCY_BLEND_COMPLETENESS_POWER),
        ANCHOR_DELAY_BLEND_K_CONVERSIONS: numOr(forecasting.ANCHOR_DELAY_BLEND_K_CONVERSIONS, ANCHOR_DELAY_BLEND_K_CONVERSIONS),
        LATENCY_MAX_MEAN_MEDIAN_RATIO: numOr(forecasting.LATENCY_MAX_MEAN_MEDIAN_RATIO, LATENCY_MAX_MEAN_MEDIAN_RATIO),
        ONSET_MASS_FRACTION_ALPHA: numOr(forecasting.ONSET_MASS_FRACTION_ALPHA, ONSET_MASS_FRACTION_ALPHA),
        ONSET_AGGREGATION_BETA: numOr(forecasting.ONSET_AGGREGATION_BETA, ONSET_AGGREGATION_BETA),
        BAYES_LOG_KAPPA_MU: numOr(forecasting.BAYES_LOG_KAPPA_MU, BAYES_LOG_KAPPA_MU),
        BAYES_LOG_KAPPA_SIGMA: numOr(forecasting.BAYES_LOG_KAPPA_SIGMA, BAYES_LOG_KAPPA_SIGMA),
        BAYES_FALLBACK_PRIOR_ESS: numOr(forecasting.BAYES_FALLBACK_PRIOR_ESS, BAYES_FALLBACK_PRIOR_ESS),
        BAYES_DIRICHLET_CONC_FLOOR: numOr(forecasting.BAYES_DIRICHLET_CONC_FLOOR, BAYES_DIRICHLET_CONC_FLOOR),
        BAYES_SIGMA_FLOOR: numOr(forecasting.BAYES_SIGMA_FLOOR, BAYES_SIGMA_FLOOR),
        BAYES_MU_PRIOR_SIGMA_FLOOR: numOr(forecasting.BAYES_MU_PRIOR_SIGMA_FLOOR, BAYES_MU_PRIOR_SIGMA_FLOOR),
        BAYES_MATURITY_FLOOR: numOr(forecasting.BAYES_MATURITY_FLOOR, BAYES_MATURITY_FLOOR),
        BAYES_SOFTPLUS_SHARPNESS: numOr(forecasting.BAYES_SOFTPLUS_SHARPNESS, BAYES_SOFTPLUS_SHARPNESS),
        BAYES_RHAT_THRESHOLD: numOr(forecasting.BAYES_RHAT_THRESHOLD, BAYES_RHAT_THRESHOLD),
        BAYES_ESS_THRESHOLD: numOr(forecasting.BAYES_ESS_THRESHOLD, BAYES_ESS_THRESHOLD),
        BAYES_WARM_START_RHAT_MAX: numOr(forecasting.BAYES_WARM_START_RHAT_MAX, BAYES_WARM_START_RHAT_MAX),
        BAYES_WARM_START_ESS_MIN: numOr(forecasting.BAYES_WARM_START_ESS_MIN, BAYES_WARM_START_ESS_MIN),
        BAYES_HDI_PROB: numOr(forecasting.BAYES_HDI_PROB, BAYES_HDI_PROB),
        BAYES_DRAWS: numOr(forecasting.BAYES_DRAWS, BAYES_DRAWS),
        BAYES_TUNE: numOr(forecasting.BAYES_TUNE, BAYES_TUNE),
        BAYES_CHAINS: numOr(forecasting.BAYES_CHAINS, BAYES_CHAINS),
        BAYES_TARGET_ACCEPT: numOr(forecasting.BAYES_TARGET_ACCEPT, BAYES_TARGET_ACCEPT),
      };
    } catch {
      return {
        RECENCY_HALF_LIFE_DAYS, LATENCY_MIN_EFFECTIVE_SAMPLE_SIZE,
        DEFAULT_T95_DAYS, FORECAST_BLEND_LAMBDA,
        LATENCY_BLEND_COMPLETENESS_POWER, ANCHOR_DELAY_BLEND_K_CONVERSIONS,
        LATENCY_MAX_MEAN_MEDIAN_RATIO, ONSET_MASS_FRACTION_ALPHA,
        ONSET_AGGREGATION_BETA,
        BAYES_LOG_KAPPA_MU, BAYES_LOG_KAPPA_SIGMA,
        BAYES_FALLBACK_PRIOR_ESS, BAYES_DIRICHLET_CONC_FLOOR,
        BAYES_SIGMA_FLOOR, BAYES_MU_PRIOR_SIGMA_FLOOR,
        BAYES_MATURITY_FLOOR, BAYES_SOFTPLUS_SHARPNESS,
        BAYES_RHAT_THRESHOLD, BAYES_ESS_THRESHOLD,
        BAYES_WARM_START_RHAT_MAX, BAYES_WARM_START_ESS_MIN,
        BAYES_HDI_PROB, BAYES_DRAWS, BAYES_TUNE, BAYES_CHAINS,
        BAYES_TARGET_ACCEPT, UPSTREAM_COVERAGE_THRESHOLD,
      };
    }
  }
}

export const forecastingSettingsService = new ForecastingSettingsService();


